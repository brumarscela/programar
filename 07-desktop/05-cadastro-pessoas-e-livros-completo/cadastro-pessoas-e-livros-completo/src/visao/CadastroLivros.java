    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // montar livro
        Livro novo = new Livro (txtTitulo.getText(), txtAutores.getText(), 
                txtAno.getText(), txtEditora.getText());
        // adicionar
        dao.adicionarLivro(novo);
        // mostrar confirmação
        JOptionPane.showMessageDialog(null, "Livro adicionado!");
        // manda o foco para o campo título
        txtTitulo.requestFocus();
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        // obter o livro marcado
        String atual = lstLivros.getSelectedValue();
        // obter o titulo do livro
        String[] partes = atual.split("/");
        // buscar o livro com base no título
        Livro paraApagar = dao.buscarLivroPorTitulo(partes[0]);
        // apagar o livro
        dao.removerLivro(paraApagar);
        // notifica
        JOptionPane.showMessageDialog(null, "Livro excluído");
        // move o foco para o campo de busca
        txtBuscarLivro.requestFocus();
    }//GEN-LAST:event_btRemoverActionPerformed

