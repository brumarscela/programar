    private void txtBuscarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscarLivroActionPerformed

        // inicializar retorno da busca
        ArrayList<Livro> busca;
        // não foi informado critério de busca?
        if (txtBuscarLivro.getText().length() == 0) {
            // mostrar todos os livros
            busca = dao.retornarLivros();
        } else {
            // buscar o livro digitado 
            busca = dao.retornarLivrosPorTextoDeBusca(txtBuscarLivro.getText());
        }
        // preencher lista com dados encontrados
        DefaultListModel df = new DefaultListModel();
        for (Livro livro : busca) {
            df.addElement(livro.getTitulo() + "/" + livro.getAutores() + "/" + livro.getAno() + "/" + livro.getEditora());
        }
        // associar a lista modelo à lista da tela
        lstLivros.setModel(df);
    }//GEN-LAST:event_txtBuscarLivroActionPerformed

